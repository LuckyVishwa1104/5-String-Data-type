#11). lower() - this function convert all the letter of string to lower case

# syntax -  str_name.lower()
print("Lucky and Nikhil".lower())
print("lucky and nikhil".lower())

# assigning to variables
a="LUCKY AND NIKHIL"
print(a.lower())
b="klwdxopw"
print(b.lower)

# using with multiline string
aa='''Hello you,
How are you.
Hope you are doing WELL.'''
print(aa.lower())

# islower() - it will return true value when all the letters of string are lower case
a="lower case"
print(a.islower())
print("KSJLkdm".islower())