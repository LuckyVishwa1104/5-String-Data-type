#10). upper() - this function will convert all the word in string to upper case letters

# syntax - str_name.upper()
print("india is great".upper())
print("INDIA IS MY COUNTRY".upper())

# assigning to variables
a='lucky and nikhil'
print(a.upper())
b='hello you'
print(b.upper())

# using with multiline string
print('''india is my country.
all indians age my brothers and sisters.
i love MY COUNTRY.'''.upper())

# isupper() - thos method will return true if all letters in string are capital
a="LUCKY"
print(a.isupper())
