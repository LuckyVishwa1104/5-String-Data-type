#6) sorted() - this method will sort the element of string according to the ascii value
# Syntax -  sorted(string_name)
str1='abcdfeghjiklnmopqrtsvuwxzy'
print(sorted(str1))
str2='3478569201'
print(sorted(str2))
# assigning to variable
str3='!@$%^&*/'
a=sorted(str3)
print(a)
b=sorted("agimdig68741@$#4")
print(b)
# passing as argument
print(sorted(".,';:-*"))