# constants- there are many constants available in string modules
import string

# string.ascii_letters - it contains all letters upper case as well lower case
print(string.ascii_letters)

# string.ascii_lowercase - it contains all the lower case letters
print(string.ascii_lowercase)

# string.ascii_uppercase - it contains alll the upper case letters
print(string.ascii_uppercase)

#string.digit - it contains all the decimal degits
print(string.digits)

# string.punctuations - it contains all the characters 
print(string.punctuation)

# string.octdigits - it contains all the octal digits
print(string.octdigits)

# string.hexdigits - it contains all the hexadecimal digits
print(string.hexdigits)